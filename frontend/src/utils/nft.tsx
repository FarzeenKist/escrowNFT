export function useOwnerOf(
    contract: Contract,
    tokenId: string,
    owner: string | undefined
) {
    const { value, error } = useCall(contract && tokenId && owner && {
        contract:contract,
        method:"ownerOf",
        args:[tokenId],
    }) ?? {};
    if(error) {
        return "Error";
    }
    if(tokenId === "") {
        return undefined;
    }else {
        if(value?.[0] === owner) {
            return true;
        }else {
            return false;
        }
    }
}

export function useGetApprove(contract: Contract, tokenId: string) {
    const { value, error } =
        useCall(
            contract &&
                tokenId && {
                    contract: contract,
                    method: "getApproved",
                    args: [tokenId],
                }
        ) ?? {};
    if (error) {
        return "Error";
    }
    if(tokenId === "") {
        return undefined;
    }else {
        if (value?.[0] === escrowContract) {
            return true;
        } else {
            return false;
        }
    }
}

export function useApprove(contract: Contract) {
    const { state, send } = useContractFunction(contract, "approve");
    const approve = send;
    const successApprove = state.status === "Success";
    const errorApprove = state.status === "Fail" || state.status === "Exception";
    return {
        successApprove,
        errorApprove,
        approve,
    };
}
